package com.example.halcyonjuly7.assessment1;

public class Car {
    String model;
    String _type;
    String year;


    public Car(String model, String _type, String year) {
        this.model = model;
        this._type = _type;
        this.year = year;
    }

    public String getModel() {
        return model;
    }

    public String get_type() {
        return _type;
    }

    public String getYear() {
        return year;
    }


}
